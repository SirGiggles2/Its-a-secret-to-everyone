#!/usr/bin/env python3
"""Check which unique room IDs are actually used."""

ROOM_ATTRS_D = [
    0x00,0x01,0x02,0x03,0x04,0x85,0x86,0x07,0x06,0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,
    0x0F,0x90,0x11,0x92,0x13,0x94,0x15,0x16,0x17,0x18,0x19,0x1A,0x1B,0x1C,0x1D,0x1E,
    0x1F,0x20,0x21,0x22,0x23,0x24,0x25,0x26,0x27,0x28,0x29,0xAA,0x2B,0xAC,0x2D,0x2E,
    0x2F,0x30,0xB1,0x32,0x33,0x34,0x35,0x36,0xB7,0x38,0xB9,0x3A,0x0A,0x3B,0xBC,0x3D,
    0x3E,0x3F,0x38,0x38,0x40,0x41,0x42,0x43,0x44,0xC5,0x46,0x47,0xC8,0x49,0x4A,0xCB,
    0x4C,0x4D,0xCE,0xCF,0xD0,0x51,0x52,0xD3,0xD4,0x55,0x56,0xD7,0x58,0x59,0x5A,0xCB,
    0xDB,0x5C,0x5D,0xDE,0xDF,0xE0,0xE1,0x62,0x63,0x64,0xE5,0xE6,0x67,0x68,0xE9,0xEA,
    0x6B,0x6C,0xED,0x6E,0x6F,0xF0,0x71,0x72,0x73,0x74,0x06,0x75,0x76,0x76,0x77,0x78
]

unique_ids = set(v & 0x3F for v in ROOM_ATTRS_D)
print(f"Unique IDs actually used: {sorted(unique_ids)}")
print(f"Count: {len(unique_ids)}")
print(f"Max unique ID: 0x{max(unique_ids):02X}")

high_ids = [uid for uid in unique_ids if uid >= 0x39]
print(f"\nUnique IDs >= 0x39: {[f'0x{uid:02X}' for uid in sorted(high_ids)]}")

if high_ids:
    print("\nRooms using these high unique IDs:")
    for uid in sorted(high_ids):
        rooms = [i for i, v in enumerate(ROOM_ATTRS_D) if (v & 0x3F) == uid]
        print(f"  Unique 0x{uid:02X}: rooms {[f'0x{r:02X}' for r in rooms]}")

print(f"\nNES ROM has 912 bytes = {912//16} unique layouts (0x00-0x{912//16-1:02X})")
print(f"But RoomAttrsOW_D references unique IDs up to 0x{max(unique_ids):02X}")

if max(unique_ids) >= 912//16:
    print(f"\nERROR: Unique IDs 0x{912//16:02X}-0x{max(unique_ids):02X} are referenced but have no layout data!")
